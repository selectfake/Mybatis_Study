public class TiaoJian {
  private Integer id;
  private Integer leixingId;
  private String ming;
  private String yanYuan;
  private String daoYan;
  private Float diJia;
  private Float gaoJia;

  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }

  public Integer getLeixingId() {
    return leixingId;
  }

  public void setLeixingId(Integer leixingId) {
    this.leixingId = leixingId;
  }

  public String getMing() {
    return ming;
  }

  public void setMing(String ming) {
    this.ming = ming;
  }

  public String getYanYuan() {
    return yanYuan;
  }

  public void setYanYuan(String yanYuan) {
    this.yanYuan = yanYuan;
  }

  public String getDaoYan() {
    return daoYan;
  }

  public void setDaoYan(String daoYan) {
    this.daoYan = daoYan;
  }

  public Float getDiJia() {
    return diJia;
  }

  public void setDiJia(Float diJia) {
    this.diJia = diJia;
  }

  public Float getGaoJia() {
    return gaoJia;
  }

  public void setGaoJia(Float gaoJia) {
    this.gaoJia = gaoJia;
  }
}
